package kr.co.goodee.dto;

public class Preference {
	
    private int MIdx;
    private  String MP1;
    private  String MP2;
    private  String MP3;
	public final int getMIdx() {
		return MIdx;
	}
	public final void setMIdx(int mIdx) {
		MIdx = mIdx;
	}
	public final String getMP1() {
		return MP1;
	}
	public final void setMP1(String mP1) {
		MP1 = mP1;
	}
	public final String getMP2() {
		return MP2;
	}
	public final void setMP2(String mP2) {
		MP2 = mP2;
	}
	public final String getMP3() {
		return MP3;
	}
	public final void setMP3(String mP3) {
		MP3 = mP3;
	}

    
}
