package kr.co.goodee.dto;


public class SeoulAreaDTO {
	private int SIdx;
	private String SName;
	private String SX;
	private String SY;
	private int SPark;
	private int SSubway;
	private int SHospital;
	private int SSum;
	private String SGraph;
	
	
	public final int getSIdx() {
		return SIdx;
	}
	public final void setSIdx(int sIdx) {
		SIdx = sIdx;
	}
	public final String getSName() {
		return SName;
	}
	public final void setSName(String sName) {
		SName = sName;
	}
	public final String getSX() {
		return SX;
	}
	public final void setSX(String sX) {
		SX = sX;
	}
	public final String getSY() {
		return SY;
	}
	public final void setSY(String sY) {
		SY = sY;
	}
	public final int getSPark() {
		return SPark;
	}
	public final void setSPark(int sPark) {
		SPark = sPark;
	}
	public final int getSSubway() {
		return SSubway;
	}
	public final void setSSubway(int sSubway) {
		SSubway = sSubway;
	}
	public final int getSHospital() {
		return SHospital;
	}
	public final void setSHospital(int sHospital) {
		SHospital = sHospital;
	}
	public final int getSSum() {
		return SSum;
	}
	public final void setSSum(int sSum) {
		SSum = sSum;
	}
	public final String getSGraph() {
		return SGraph;
	}
	public final void setSGraph(String sGraph) {
		SGraph = sGraph;
	}
	
	

}
